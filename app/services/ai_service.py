def generate_response(message: str, assistant: str) -> str:
    text = message.lower()

    if assistant == "customer":

        if "не хочет" in text or "отказ" in text:
            return (
                "Здравствуйте!\n\n"
                "Спасибо, что сообщили нам. Мы понимаем ваше решение. "
                "Подскажите, пожалуйста, что стало причиной отказа? "
                "Возможно, мы сможем предложить более удобное решение."
            )

        elif "доставка" in text:
            return (
                "Здравствуйте!\n\n"
                "Приносим извинения за задержку доставки. "
                "Мы уже проверяем статус вашего заказа и сообщим вам информацию "
                "как можно скорее."
            )

        elif "возврат" in text:
            return (
                "Здравствуйте!\n\n"
                "Мы готовы помочь с оформлением возврата. "
                "Пожалуйста, сообщите номер заказа, и мы объясним дальнейшие шаги."
            )

        else:
            return (
                "Я помогу подготовить профессиональный ответ клиенту. "
                "Опишите ситуацию немного подробнее."
            )

    elif assistant == "marketing":
        return (
            f"✍️ Идея для маркетинга:\n\n"
            f"Создайте рекламный пост на тему: «{message}».\n"
            "Добавьте призыв к действию и специальное предложение для клиентов."
        )

    elif assistant == "sales":
        return (
            f"💼 Предложение для клиента:\n\n"
            f"На основе запроса «{message}» подготовьте краткое коммерческое предложение, "
            "подчеркнув выгоды и следующий шаг для клиента."
        )

    elif assistant == "documents":
        return (
            f"📄 Я помогу оформить документ по теме:\n\n{message}"
        )

    return "Неизвестный тип помощника."